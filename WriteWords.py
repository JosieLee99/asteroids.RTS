    def measure_text(self, text, height=20, spacing=4):
        height = min(20, height)
        scale = height / 6
        char_width = 4 * scale
        lines = text.split("\n")
        widest_line = 0

        for line in lines:
            if len(line) == 0:
                continue

            line_width = len(line) * char_width + (len(line) - 1) * spacing
            widest_line = max(widest_line, line_width)

        text_height = len(lines) * height + max(0, len(lines) - 1) * spacing
        return round(widest_line), round(text_height)

    def draw_text(self, screen, text, pos, color=(255, 255, 255), height=20, line_width=1, spacing=4):
        height = min(20, height)
        scale = height / 6
        char_width = 4 * scale
        start_x, start_y = pos
        x = start_x
        y = start_y

        for character in text:
            if character == "\n":
                x = start_x
                y += height + spacing
                continue

            if character == " ":
                x += char_width + spacing
                continue

            self.draw_character(screen, character, (x, y), color, scale, line_width)
            x += char_width + spacing

    def draw_character(self, screen, character, pos, color, scale, line_width):
        glyph = self.glyphs.get(character.upper())
        if glyph is None:
            return

        x, y = pos
        for line in glyph:
            points = [(round(x + point[0] * scale), round(y + point[1] * scale)) for point in line]
            pygame.draw.lines(screen, color, False, points, line_width)
